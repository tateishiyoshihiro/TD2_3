Matrix4x4 MakePerspectiveFovMatrix(float fovY, float aspectRatio, float nearClip, float farClip) {
	Matrix4x4 Per;

	Per.m[0][0] = (1 / aspectRatio) * (1 / tan(fovY / 2));
	Per.m[0][1] = 0.0f;
	Per.m[0][2] = 0.0f;
	Per.m[0][3] = 0.0f;

	Per.m[1][0] = 0.0f;
	Per.m[1][1] = (1 / tan(fovY / 2));
	Per.m[1][2] = 0.0f;
	Per.m[1][3] = 0.0f;

	Per.m[2][0] = 0.0f;
	Per.m[2][1] = 0.0f;
	Per.m[2][2] = farClip / farClip - nearClip;
	Per.m[2][3] = 1.0f;

	Per.m[3][0] = 0.0f;
	Per.m[3][1] = 0.0f;
	Per.m[3][2] = -nearClip * farClip / (farClip - nearClip);
	Per.m[3][3] = 0.0f;

	return Per;
}

Matrix4x4 MakeOrthographicMatrix(
	float left, float top, float right, float bottom, float nearClip, float farClip) {
	Matrix4x4 result;

	result.m[0][0] = 2 / (right - left);
	result.m[0][1] = 0;
	result.m[0][2] = 0;
	result.m[0][3] = 0;

	result.m[1][0] = 0;
	result.m[1][1] = 2 / (top - bottom);
	result.m[1][2] = 0;
	result.m[1][3] = 0;

	result.m[2][0] = 0;
	result.m[2][1] = 0;
	result.m[2][2] = 1 / (farClip - nearClip);
	result.m[2][3] = 0;

	result.m[3][0] = (left + right) / (left - right);
	result.m[3][1] = (top + bottom) / (bottom - top);
	result.m[3][2] = nearClip / (nearClip - farClip);
	result.m[3][3] = 1;

	return result;
}

Matrix4x4 Multiply(float scalar, const Matrix4x4& m) {

	Matrix4x4 result;

	for (int y = 0; y < 4; y++) {
		for (int x = 0; x < 4; x++) {
			result.m[y][x] = scalar * m.m[y][x];
		}
	}
	return result;
}

Matrix4x4 MakeRotateAxisAngle(const Vector3& axis, float angele) {
	Matrix4x4 rS = MakeIdentity4x4();

	rS.m[0][0] = std::cosf(angele);
	rS.m[1][1] = std::cosf(angele);
	rS.m[2][2] = std::cosf(angele);

	Matrix4x4 rP = MakeIdentity4x4();

	rP.m[0][0] = axis.x * axis.x;
	rP.m[0][1] = axis.x * axis.y;
	rP.m[0][2] = axis.x * axis.z;

	rP.m[1][0] = axis.y * axis.x;
	rP.m[1][1] = axis.y * axis.y;
	rP.m[1][2] = axis.y * axis.z;

	rP.m[2][0] = axis.z * axis.x;
	rP.m[2][1] = axis.z * axis.y;
	rP.m[2][2] = axis.z * axis.z;

	rP.m[3][3] = 0.0f;

	rP = Multiply((1.0f - std::cosf(angele)), rP);

	Matrix4x4 rC = MakeIdentity4x4();

	rC.m[0][0] = 0.0f;
	rC.m[0][1] = -axis.z;
	rC.m[0][2] = axis.y;
	rC.m[1][0] = axis.z;
	rC.m[1][1] = 0.0f;
	rC.m[1][2] = -axis.x;
	rC.m[2][0] = -axis.y;
	rC.m[2][1] = axis.x;
	rC.m[2][2] = 0.0f;
	rC.m[3][3] = 0.0f;

	rC = Multiply((-std::sinf(angele)), rC);

	Matrix4x4 resultMatrix = Add(Add(rS, rP), rC);

	return resultMatrix;
}
